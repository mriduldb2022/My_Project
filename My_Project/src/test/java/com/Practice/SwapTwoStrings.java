package com.Practice;

public class SwapTwoStrings {

	public static void main(String[] args) {
		String a = "Hellow";
		String b = "World";
		System.out.println("Before swap string");
		System.out.println(a);
		System.out.println(b);
		System.out.println(".........");
		
		String c;
		c = a;	
		a = b; 
		b = c;
		System.out.println("After swap");
		System.out.println(a);
		System.out.println(b);
		
		
		
		
		
		
	}
}
