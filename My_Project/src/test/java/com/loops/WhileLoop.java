package com.loops;

public class WhileLoop {

	public static void repeatMoreNumbers() {
		int i = 10;
		
		//while loops use true or false condition. If you don't 
		// know the number of times to repeat the value, use the while condition.
		while (i == 10) {
			
			System.out.println(i);
		}
	}
	public static void main(String[] args) {
		WhileLoop.repeatMoreNumbers();
	}
}
